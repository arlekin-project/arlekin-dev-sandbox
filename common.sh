#!/bin/bash

#Get current file parent directory
SOURCE="${BASH_SOURCE[0]}"
while [ -h "$SOURCE" ]; do
  DIR="$( cd -P "$( dirname "$SOURCE" )" && pwd )"
  SOURCE="$(readlink "$SOURCE")"
  [[ $SOURCE != /* ]] && SOURCE="$DIR/$SOURCE"
done
DIR="$( cd -P "$( dirname "$SOURCE" )" && pwd )"

#Defined
DBAL_REPOSITORY_SHORTNAME='dbal'
DOCKER_DEV_REPOSITORY_SHORTNAME='docker-dev'

PROJECT_NAME='calam'
GITHUB_ORGANIZATION=$PROJECT_NAME'-project'

PHP_CONTAINER_NAME=$PROJECT_NAME'_php_1'

REPOSITORIES=$DBAL_REPOSITORY_SHORTNAME" "$DOCKER_DEV_REPOSITORY_SHORTNAME

#Computed
FOLDERS=$REPOSITORIES
DOCKER_DEV_REPOSITORY_FULLPATH=$DIR/$DOCKER_DEV_REPOSITORY_SHORTNAME
